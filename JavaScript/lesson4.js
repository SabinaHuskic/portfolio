// RELATIONAL OR COMPARIOSON OPERATORS

/*
> - more than
< - less than
>= - more than equal
<= - less than equal
*/
console.log("RELATIONAL OR COMPARIOSON OPERATORS")
var result = 5 < 7
console.log(result)

result = 5 > 7
console.log(result)

result = 5 <= 5
console.log(result)

result = 5 >= 5
console.log(result)


// EQUALITY OPERATORS
console.log("EQUALITY OPERATORS")
var x = 1
console.log(x == '1') // loose comparison - we are just comparing the value
console.log(x === '1') // strict comparison - we are checking both the value and its data type (1 as a nuber is not the same as '1' as a string)