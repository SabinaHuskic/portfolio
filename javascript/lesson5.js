// LOGICAL "AND"

console.log(true && true) // all values have to be TRUE for expression to be true
console.log(true && false)

// LOGICAL "OR"

console.log(true || true) // any one value has to be TRUE for expression to be true
console.log(true || false)
console.log(false || false)

// LOGICAL "NOT"
console.log(!true) // opposite from the value inside
console.log(6 !== 10)
